function createNumberSequence(start, end, containerId) {
  const container = document.getElementById(containerId);
  if (!container) return;
  for (let i = start; i <= end; i++) {
    const p = document.createElement("p");
    p.className = "null";
    p.innerText = i;
    container.appendChild(p);
  }
}

